// Synthetic benchmark — measure scan performance on a generated
// workload. Used by `regent benchmark` and CI regression gates.
//
// Generates `N` synthetic files (each a few KB of plausible-looking
// code with a mix of pattern hits and misses) and `M` synthetic
// detect rules. Runs `runRules` end-to-end, reports per-iteration
// wall-clock time, and compares against `.regent/baseline.json`
// when present.

import { mkdtempSync, rmSync, writeFileSync, mkdirSync, existsSync, readFileSync } from 'node:fs';
import { tmpdir } from 'node:os';
import { join } from 'node:path';

import { runRules } from '../runner.js';
import { defineRule } from '../define-rule.js';
import type { CompiledRule, RuleSpec } from '../types.js';

/**
 * Inputs to `runBenchmark`. Sizes scale linearly with `files` and `rules`
 * — keep both small for a quick smoke test (e.g. 50/50/3) and large
 * for a realistic regression measurement (e.g. 2000/100/5).
 */
export interface BenchmarkOptions {
  readonly files: number;
  readonly rules: number;
  readonly iterations: number;
}

/**
 * Per-iteration timings + aggregate statistics + (when a baseline
 * exists) percentage drift vs. the recorded baseline. `baselineDeltaPct`
 * is `null` on a first run, when no `.regent/baseline.json` is on disk.
 */
export interface BenchmarkResult {
  readonly files: number;
  readonly rules: number;
  readonly iterations: number;
  readonly durationsMs: readonly number[];
  readonly medianMs: number;
  readonly meanMs: number;
  readonly minMs: number;
  readonly maxMs: number;
  readonly baselineDeltaPct: number | null;
}

function generateFileContent(seed: number): string {
  const lines: string[] = [];
  lines.push('// Auto-generated by regent benchmark');
  lines.push('namespace Sample;');
  for (let i = 0; i < 40; i++) {
    if ((seed + i) % 7 === 0) {
      lines.push('    #region generated-block');
      lines.push('    int x;');
      lines.push('    #endregion');
    } else if ((seed + i) % 11 === 0) {
      lines.push('    // TODO generated-line');
    } else {
      lines.push(`    public void Method${i}() { Console.WriteLine(${i}); }`);
    }
  }
  lines.push('}');
  return lines.join('\n');
}

function generateSyntheticRules(count: number): RuleSpec[] {
  const out: RuleSpec[] = [];
  for (let i = 0; i < count; i++) {
    out.push(
      defineRule({
        id: `bench.rule-${i}`,
        severity: i % 2 === 0 ? 'error' : 'warning',
        pattern: i % 3 === 0 ? '#region\\b' : `Method${i}\\b`,
        globs: ['**/*.cs'],
        message: `bench rule ${i}`,
      }),
    );
  }
  return out;
}

/**
 * Run a synthetic benchmark: generate `files` synthetic `.cs` files
 * and `rules` synthetic regex rules in a tmpdir, then run `runRules`
 * `iterations` times end-to-end. Writes `.regent/baseline.json` on
 * first run; reports drift vs. that baseline on subsequent runs.
 *
 * @param options file / rule / iteration counts (see {@link BenchmarkOptions})
 * @returns aggregate timings + baseline drift (see {@link BenchmarkResult})
 */
export async function runBenchmark(
  options: BenchmarkOptions,
): Promise<BenchmarkResult> {
  const tmp = mkdtempSync(join(tmpdir(), 'regent-bench-'));
  try {
    for (let i = 0; i < options.files; i++) {
      writeFileSync(join(tmp, `file-${i}.cs`), generateFileContent(i));
    }
    const rules = generateSyntheticRules(options.rules).map<CompiledRule>((spec) => ({
      spec,
      source: '<bench>',
      origin: { kind: 'repo', path: tmp },
    }));
    const durations: number[] = [];
    for (let i = 0; i < options.iterations; i++) {
      const start = performance.now();
      await runRules(rules, {
        cwd: tmp,
        includeGlobs: ['**/*.cs'],
        excludeGlobs: [],
        changedOnly: false,
        diffBase: 'HEAD',
      });
      durations.push(performance.now() - start);
    }
    const sorted = [...durations].sort((a, b) => a - b);
    const median = sorted[Math.floor(sorted.length / 2)] ?? 0;
    const mean = durations.reduce((a, b) => a + b, 0) / durations.length;

    // Compare against baseline if present.
    let baselineDeltaPct: number | null = null;
    const baselinePath = join(process.cwd(), '.regent', 'baseline.json');
    if (existsSync(baselinePath)) {
      try {
        const baseline = JSON.parse(readFileSync(baselinePath, 'utf8')) as {
          medianMs?: number;
        };
        if (typeof baseline.medianMs === 'number' && baseline.medianMs > 0) {
          baselineDeltaPct = ((median - baseline.medianMs) / baseline.medianMs) * 100;
        }
      } catch {
        // ignore malformed baseline
      }
    } else {
      // First run — write the baseline so subsequent runs have a target.
      mkdirSync(join(process.cwd(), '.regent'), { recursive: true });
      writeFileSync(
        baselinePath,
        JSON.stringify(
          { medianMs: median, files: options.files, rules: options.rules, recordedAt: Date.now() },
          null,
          2,
        ),
      );
    }

    return {
      files: options.files,
      rules: options.rules,
      iterations: options.iterations,
      durationsMs: durations,
      medianMs: median,
      meanMs: mean,
      minMs: sorted[0] ?? 0,
      maxMs: sorted[sorted.length - 1] ?? 0,
      baselineDeltaPct,
    };
  } finally {
    rmSync(tmp, { recursive: true, force: true });
  }
}